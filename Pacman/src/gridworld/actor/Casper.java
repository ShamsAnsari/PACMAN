    /**
     * Uses Depth first search to find a path to Pacman.
     *
     * @param maze The 2d array
     * @param x    The current row of the Ghost
     * @param y    The current col of the Ghost
     * @param path A list of Points
     * @return
     */
    private boolean DFS(
                    int[][] maze, int x, int y, List<Point> path )
    {
        if ( !grid.isValid( new Location( x, y ) ) )
        {
            return false;
        }
        //Target
        if ( maze[x][y] == 9 )
        {
            path.add( new Point( x, y ) );
            return true;
        }

        //Changing point to point visited
        if ( maze[x][y] == 0 )
        {

            maze[x][y] = 2;

            //Searching one down
            if ( DFS( maze, x + 1, y, path ) )
            {
                path.add( new Point( x, y ) );
                return true;
            }

            //Searching one left
            if ( DFS( maze, x, y - 1, path ) )
            {
                path.add( new Point( x, y ) );
                return true;
            }

            //Searching one up
            if ( DFS( maze, x - 1, y, path ) )
            {
                path.add( new Point( x, y ) );
                return true;
            }

            //Searching one right
            if ( DFS( maze, x, y + 1, path ) )
            {
                path.add( new Point( x, y ) );
                return true;
            }

        }

        return false;

    }


    /**
     * Resets the maze to original maze after DFS changes the numbers in itz
     */
    private void resetMaze()
    {
        for ( int r = 0; r < OriginalMap.length; r++ )
        {
            for ( int c = 0; c < OriginalMap[0].length; c++ )
            {
                maze[r][c] = OriginalMap[r][c];
            }
        }

    }



